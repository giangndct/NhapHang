package Entity;

import java.io.Serializable;

/**
 *
 * @author Admin
 */
public class User205  {

    private int id;
    private String name, username, password, role;

    public User205() {
    }

    public User205(int id, String name, String username, String password, String role) {
        this.id = id;
        this.name = name;
        this.username = username;
        this.password = password;
        this.role = role;
    }
    
     public User205(String username, String password) {
        this.username = username;
        this.password = password;
    }

    public User205(User205 u) {
        this.id = u.id;
        this.name = u.name;
        this.username = u.username;
        this.password = u.password;
        this.role = u.role;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }
    
}
